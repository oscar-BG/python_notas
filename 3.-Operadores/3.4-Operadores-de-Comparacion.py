# == devuelve true si ambos valores son iguales o false si son diferentes
a = 4
b = 2

resultado = a == b
print(f'Resultado == {resultado}')

# != Diferente de, devuelve true si ambos valores no son iguales y false si son iguales
resultado = a != b
print(f'Resultado !=  {resultado}')

# > mayor, devuel true si el primero valor es mayor al segundo y false si el primero valor es menor al segundo
resultado = a > b
print(f'Resultado >  {resultado}')

#  >=, mayor o igual
resultado = a >= b
print(f'Resultado >=  {resultado}')

# < menor
resultado = a < b
print(f'Resultado <  {resultado}')

# <= menor o igual
resultado = a <= b
print(f'Resultado <=  {resultado}')


