# Operadores
# Suma  +
# Resta -
# Multiplicacion *
# Division /
# Integer divicion //
# Expoentiation **
# Modulo %

operandoA = 3
operandoB = 2

# Suma 
suma = operandoA + operandoB
print('Resultado de suma: ', suma)

#A esto se le llama interpolacion (Incluir variables en una cadena)
print(f'Resultado suma: {suma}')

resta = operandoA - operandoB

print(f'Resultado resta {resta}')

multiplicacion = operandoA * operandoB
print(f'resultado multiplicacion: {multiplicacion}')

division = operandoA / operandoB
print(f'resultado division: {division}')

#Division con valor entero
divisionE = operandoA // operandoB
print(f'resultado division entero: {divisionE}')

modulo = operandoA % operandoB
print(f'resultado del modulo: {modulo}')

# Exponente
exponente = operandoA ** operandoB
print(f'resultado del exponente: {exponente}')


